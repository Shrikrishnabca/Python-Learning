# Data type is a type of data store in particular memory location
# We didn't need to specify the data type, automatically binds during the run time
# So python is a dynamic memory allocation

# To know the type of data store on the variable we have to use built in function type()
a = 10
print(type(a))

# dir is built in function returns the all the attributes attached to the object
print(dir(a))

